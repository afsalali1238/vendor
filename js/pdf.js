// ================================================================

export async function generateInvoicePDF(job, vendor, telrPaymentUrl) {
  const { jsPDF } = window.jspdf;
  const doc = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });
  const pageW = doc.internal.pageSize.getWidth();
  const now = new Date();
  const dueDate = new Date(now.getTime() + 30 * 24 * 60 * 60 * 1000);

  addHeader(doc, vendor, 'TAX INVOICE', `INV-${job.job_code}`);

  let y = 34;

  // UAE FTA required banner
  doc.setFillColor(232, 245, 233);
  doc.rect(14, y, pageW - 28, 7, 'F');
  doc.setFontSize(7);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(27, 94, 32);
  doc.text('UAE VAT COMPLIANT TAX INVOICE — Federal Tax Authority Registered', pageW / 2, y + 5, { align: 'center' });
  doc.setTextColor(0, 0, 0);
  y += 12;

  y = section(doc, y, 'INVOICE DETAILS');
  y = infoTable(doc, y, [
    ['Invoice number', `INV-${job.job_code}`],
    ['PO reference', `PO-${job.job_code}`],
    ['Job reference', job.job_code],
    ['Invoice date', now.toLocaleDateString('en-AE')],
    ['Due date', dueDate.toLocaleDateString('en-AE')],
    ['Tax period', `${now.toLocaleDateString('en-AE', { month:'long', year:'numeric' })}`],
  ]);

  y += 4;
  y = section(doc, y, 'BILL TO');
  y = infoTable(doc, y, [
    ['Company', job.client_company],
    ['Contact', job.client_name],
    ['Phone', job.client_phone],
    ['Email', job.client_email || '—'],
  ]);

  y += 4;
  y = section(doc, y, 'SERVICES RENDERED');
  y = infoTable(doc, y, [
    ['Service', `${job.service_type} — ${job.origin} to ${job.destination}`],
    ['Date', job.pickup_date],
    ['Driver', job.driver_name],
    ['Vehicle', job.vehicle_plate],
    ['Cargo', job.cargo_type || job.equipment_type],
    ['Delivery reference', job.epod_pdf_url ? `ePOD-${job.job_code}` : 'Pending'],
  ]);

  y += 4;
  y = section(doc, y, 'AMOUNT DUE');
  const priceExVAT = job.quoted_price / 1.05;
  const vatAmount  = job.quoted_price - priceExVAT;
  y = vatTable(doc, y, priceExVAT, vatAmount, job.quoted_price);

  // Payment link
  if (telrPaymentUrl) {
    y += 4;
    doc.setFillColor(225, 245, 254);
    doc.rect(14, y, pageW - 28, 10, 'F');
    doc.setFontSize(8);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(21, 101, 192);
    doc.text('💳 Pay online: ', 17, y + 6.5);
    doc.setFont('helvetica', 'normal');
    doc.text(telrPaymentUrl, 42, y + 6.5);
    doc.setTextColor(0, 0, 0);
    y += 15;
  }

  // Bank transfer alternative
  y = section(doc, y, 'BANK TRANSFER DETAILS');
  y = infoTable(doc, y, [
    ['Bank', vendor.bank_name || '—'],
    ['IBAN', vendor.bank_iban || '—'],
    ['Reference', `INV-${job.job_code}`],
    ['Vendor TRN', vendor.trn || '—'],
  ]);

  addFooter(doc, 1);

  return uploadPDF(doc, vendor.id, job.job_code, 'invoice.pdf', 'invoice_pdf_url', job.job_code);
}

