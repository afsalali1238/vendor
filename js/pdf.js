// 1. QUOTE PDF
// ================================================================

export async function generateQuotePDF(job, vendor) {
  const { jsPDF } = window.jspdf;
  const doc = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });
  const pageW = doc.internal.pageSize.getWidth();
  const now = new Date();
  const validUntil = new Date(now.getTime() + 48 * 60 * 60 * 1000);

  addHeader(doc, vendor, 'QUOTATION', `QUO-${job.job_code}`);

  let y = 34;

  // Alert bar
  doc.setFillColor(255, 248, 225);
  doc.rect(14, y, pageW - 28, 8, 'F');
  doc.setFontSize(7.5);
  doc.setTextColor(...BRAND.color);
  doc.setFont('helvetica', 'bold');
  doc.text(`⏱ This quote is valid for 48 hours — until ${validUntil.toLocaleDateString('en-AE', { day:'2-digit', month:'short', year:'numeric', hour:'2-digit', minute:'2-digit' })}`, pageW / 2, y + 5.5, { align: 'center' });
  doc.setTextColor(0, 0, 0);
  y += 13;

  y = section(doc, y, 'JOB DETAILS');
  y = infoTable(doc, y, [
    ['Reference', job.job_code],
    ['Service type', job.service_type?.charAt(0).toUpperCase() + job.service_type?.slice(1)],
    ['Origin', job.origin],
    ['Destination', job.destination],
    ['Pickup date', job.pickup_date],
    ['Cargo / Equipment', job.cargo_type || job.equipment_type],
    ['Special requirements', job.special_requirements || 'None'],
    ['Quote date', now.toLocaleDateString('en-AE', { day:'2-digit', month:'short', year:'numeric' })],
  ]);

  y += 4;
  y = section(doc, y, 'PRICING');

  const priceExVAT = job.quoted_price / 1.05;
  const vatAmount  = job.quoted_price - priceExVAT;
  y = vatTable(doc, y, priceExVAT, vatAmount, job.quoted_price);

  y += 4;
  y = section(doc, y, 'VENDOR DETAILS');
  y = infoTable(doc, y, [
    ['Company', vendor.company_name],
    ['TRN', vendor.trn || '—'],
    ['Bank', vendor.bank_name || '—'],
    ['IBAN', vendor.bank_iban || '—'],
    ['Payment terms', 'Net 30 days from invoice date'],
  ]);

  // Approve / Decline buttons (text links)
  y += 8;
  doc.setFillColor(...BRAND.teal);
  doc.rect(14, y, (pageW - 32) / 2, 10, 'F');
  doc.setFontSize(9);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(...BRAND.dark);
  doc.text('✓ APPROVE QUOTE', 14 + (pageW - 32) / 4, y + 6.5, { align: 'center' });

  doc.setFillColor(240, 240, 240);
  doc.rect(14 + (pageW - 32) / 2 + 4, y, (pageW - 32) / 2, 10, 'F');
  doc.setTextColor(...BRAND.gray);
  doc.text('✗ DECLINE', 14 + (pageW - 32) / 2 + 4 + (pageW - 32) / 4, y + 6.5, { align: 'center' });

  doc.setTextColor(0, 0, 0);

  addFooter(doc, 1);

  return uploadPDF(doc, vendor.id, job.job_code, 'quote.pdf', 'quote_pdf_url', job.job_code);
}

