// ================================================================
// js/pdf.js
// All PDF generation logic using jsPDF.
// Loads jsPDF from CDN — no npm install needed.
// All functions return a Blob URL and upload to Supabase Storage.
// ================================================================

// jsPDF is loaded via <script> tag in each HTML page that needs it:
// <script src="https://cdnjs.cloudflare.com/ajax/libs/jspdf/2.5.1/jspdf.umd.min.js"></script>

import { uploadDocument } from './supabase.js';
import { formatAED } from './whatsapp.js';

// ── BRANDING ─────────────────────────────────────────────────────
const BRAND = {
  name: 'Kasper Vendor OS',
  color: [245, 158, 11],    // Gold — RGB
  dark:  [13, 27, 42],      // Navy
  teal:  [0, 212, 200],
  gray:  [120, 139, 160],
  light: [245, 245, 245],
};

// ── SHARED HEADER ────────────────────────────────────────────────

function addHeader(doc, vendor, docTitle, docRef) {
  const pageW = doc.internal.pageSize.getWidth();

  // Dark header bar
  doc.setFillColor(...BRAND.dark);
  doc.rect(0, 0, pageW, 28, 'F');

  // Kasper branding (left)
  doc.setFontSize(8);
  doc.setTextColor(...BRAND.color);
  doc.setFont('helvetica', 'bold');
  doc.text('⬡ KASPER', 14, 10);
  doc.setFontSize(7);
  doc.setTextColor(150, 170, 180);
  doc.setFont('helvetica', 'normal');
  doc.text('VENDOR OS', 14, 15);

  // Document title (center)
  doc.setFontSize(11);
  doc.setTextColor(255, 255, 255);
  doc.setFont('helvetica', 'bold');
  doc.text(docTitle, pageW / 2, 12, { align: 'center' });
  doc.setFontSize(7);
  doc.setTextColor(150, 170, 180);
  doc.setFont('helvetica', 'normal');
  doc.text(docRef, pageW / 2, 18, { align: 'center' });

  // Vendor name (right)
  doc.setFontSize(8);
  doc.setTextColor(200, 210, 220);
  doc.setFont('helvetica', 'bold');
  doc.text(vendor.company_name || '', pageW - 14, 10, { align: 'right' });
  doc.setFontSize(7);
  doc.setFont('helvetica', 'normal');
  doc.text(`TRN: ${vendor.trn || '—'}`, pageW - 14, 15, { align: 'right' });

  // Reset
  doc.setTextColor(0, 0, 0);
}

function addFooter(doc, pageNum) {
  const pageW = doc.internal.pageSize.getWidth();
  const pageH = doc.internal.pageSize.getHeight();

  doc.setFillColor(...BRAND.light);
  doc.rect(0, pageH - 14, pageW, 14, 'F');

  doc.setFontSize(7);
  doc.setTextColor(...BRAND.gray);
  doc.setFont('helvetica', 'normal');
  doc.text('Generated by Kasper Vendor OS · kasperlogistics.vercel.app', 14, pageH - 5);
  doc.text(`Page ${pageNum}`, pageW - 14, pageH - 5, { align: 'right' });
  doc.setTextColor(0, 0, 0);
}

function section(doc, y, title) {
  doc.setFillColor(...BRAND.color);
  doc.rect(14, y, 4, 6, 'F');
  doc.setFontSize(9);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(...BRAND.dark);
  doc.text(title, 21, y + 4.5);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(0, 0, 0);
  return y + 10;
}

function infoTable(doc, y, rows, col1W = 50, rowH = 7) {
  const pageW = doc.internal.pageSize.getWidth();
  const col2X = 14 + col1W;
  const col2W = pageW - 14 - col1W - 14;

  rows.forEach(([label, value], i) => {
    const bg = i % 2 === 0 ? [250, 250, 250] : [255, 255, 255];
    doc.setFillColor(...bg);
    doc.rect(14, y, col1W, rowH, 'F');
    doc.rect(col2X, y, col2W, rowH, 'F');

    doc.setDrawColor(220, 220, 220);
    doc.rect(14, y, col1W + col2W, rowH, 'S');

    doc.setFontSize(8);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(...BRAND.gray);
    doc.text(label, 17, y + rowH - 2);

    doc.setFont('helvetica', 'normal');
    doc.setTextColor(30, 30, 30);
    doc.text(String(value || '—'), col2X + 3, y + rowH - 2);

    y += rowH;
  });

  return y + 4;
}

function vatTable(doc, y, priceExVAT, vatAmount, totalIncVAT) {
  const pageW = doc.internal.pageSize.getWidth();
  const rightX = pageW - 14;

  const rows = [
    ['Amount (ex-VAT)', `AED ${formatAED(priceExVAT)}`],
    ['VAT (5%)', `AED ${formatAED(vatAmount)}`],
  ];

  doc.setFontSize(8);
  let ry = y;
  rows.forEach(([l, v]) => {
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(...BRAND.gray);
    doc.text(l, rightX - 60, ry, { align: 'right' });
    doc.setTextColor(30, 30, 30);
    doc.text(v, rightX, ry, { align: 'right' });
    ry += 6;
  });

  // Total box
  ry += 2;
  doc.setFillColor(...BRAND.dark);
  doc.rect(rightX - 80, ry - 5, 80, 9, 'F');
  doc.setFontSize(9);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(...BRAND.color);
  doc.text('TOTAL (inc-VAT)', rightX - 60, ry + 1, { align: 'right' });
  doc.setTextColor(255, 255, 255);
  doc.text(`AED ${formatAED(totalIncVAT)}`, rightX - 2, ry + 1, { align: 'right' });
  doc.setTextColor(0, 0, 0);

  return ry + 14;
}

