// 4. ePOD PDF
// ================================================================

export async function generateEPODPDF(job, vendor, { driverSigB64, clientSigB64, photoUrl, driverGPS }) {
  const { jsPDF } = window.jspdf;
  const doc = new jsPDF({ orientation: 'portrait', unit: 'mm', format: 'a4' });
  const pageW = doc.internal.pageSize.getWidth();

  addHeader(doc, vendor, 'PROOF OF DELIVERY', `ePOD-${job.job_code}`);

  let y = 34;

  // Confirmed banner
  doc.setFillColor(232, 245, 233);
  doc.rect(14, y, pageW - 28, 8, 'F');
  doc.setFontSize(9);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(27, 94, 32);
  doc.text('✓ DELIVERY CONFIRMED — BOTH PARTIES SIGNED', pageW / 2, y + 5.5, { align: 'center' });
  doc.setTextColor(0, 0, 0);
  y += 13;

  y = section(doc, y, 'DELIVERY DETAILS');
  y = infoTable(doc, y, [
    ['Job reference', job.job_code],
    ['Origin', job.origin],
    ['Destination', job.destination],
    ['Cargo', job.cargo_type || job.equipment_type],
    ['Driver', job.driver_name],
    ['Vehicle plate', job.vehicle_plate],
    ['Delivery time', job.epod_driver_at ? new Date(job.epod_driver_at).toLocaleString('en-AE') : '—'],
    ['GPS at delivery', driverGPS ? `${driverGPS.lat?.toFixed(5)}, ${driverGPS.lng?.toFixed(5)}` : '—'],
  ]);

  // Signatures
  y += 4;
  y = section(doc, y, 'SIGNATURES');

  const halfW = (pageW - 32) / 2;

  // Driver signature box
  doc.setDrawColor(200, 200, 200);
  doc.rect(14, y, halfW, 32, 'S');
  doc.setFontSize(7);
  doc.setTextColor(...BRAND.gray);
  doc.text('DRIVER SIGNATURE', 17, y + 5);
  doc.text(job.driver_name || '', 17, y + 10);

  if (driverSigB64) {
    try {
      doc.addImage(driverSigB64, 'PNG', 16, y + 12, halfW - 4, 16);
    } catch (e) { /* sig not renderable */ }
  }

  doc.setTextColor(0, 0, 0);
  doc.setFontSize(7);
  doc.text(job.epod_driver_at ? new Date(job.epod_driver_at).toLocaleString('en-AE') : '', 17, y + 29);

  // Client signature box
  doc.rect(14 + halfW + 4, y, halfW, 32, 'S');
  doc.setTextColor(...BRAND.gray);
  doc.text('CLIENT SIGNATURE', 14 + halfW + 7, y + 5);
  doc.text(job.client_name || '', 14 + halfW + 7, y + 10);

  if (clientSigB64) {
    try {
      doc.addImage(clientSigB64, 'PNG', 14 + halfW + 6, y + 12, halfW - 4, 16);
    } catch (e) { /* sig not renderable */ }
  }

  doc.setTextColor(0, 0, 0);
  doc.text(job.epod_client_at ? new Date(job.epod_client_at).toLocaleString('en-AE') : '', 14 + halfW + 7, y + 29);
  y += 36;

  // Delivery photo
  if (photoUrl) {
    y += 4;
    y = section(doc, y, 'DELIVERY PHOTO');
    doc.setFontSize(7);
    doc.setTextColor(...BRAND.gray);
    doc.text(`Photo: ${photoUrl}`, 14, y);
    // Note: embedding remote images in jsPDF requires fetching and converting to base64
    // For MVP, we show the URL. Implement image embedding in Phase 2.
    y += 8;
  }

  addFooter(doc, 1);

  return uploadPDF(doc, vendor.id, job.job_code, 'epod.pdf', 'epod_pdf_url', job.job_code);
}

// ── UPLOAD HELPER ─────────────────────────────────────────────────

async function uploadPDF(doc, vendorId, jobCode, filename, urlField, _jobCode) {
  const blob = doc.output('blob');
  const url = await uploadDocument(vendorId, jobCode, filename, blob);
  return { url, blob, dataUrl: doc.output('datauristring') };
}
